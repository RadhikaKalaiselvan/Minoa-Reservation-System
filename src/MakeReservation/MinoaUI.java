    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {                                             
       String name = custNameText.getText();
       String phone = phoneNoText.getText();
       String email = emailText.getText();
       custDetPanel.setVisible(false);
       int charges = controller.searchRecord(name, phone, email);
       if(charges >= 0){
           totalCostLabel.setText(charges+"");
    	   this.paymentScenario();
       }
       else{
    	   totalCostLabel.setText("cust no found");
       }
       
    }                                            

