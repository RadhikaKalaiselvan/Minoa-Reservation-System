    private void availButtonActionPerformed(java.awt.event.ActionEvent evt) {                                            
        String startDate = startDateText.getText();
        String endDate = endDateText.getText();
        int daysrem = controller.checkAvailability(startDate, endDate);
        custDetPanel.setVisible(true);
        if(daysrem >= 0){
            convButton.setEnabled(true);
            if(daysrem <= 30)
            incentButton.setEnabled(true);
            if(daysrem >= 60){
                advButton.setEnabled(true);
                if(daysrem >= 90)
                    prepaidButton.setEnabled(true);
            }     
        }
    }                                           

