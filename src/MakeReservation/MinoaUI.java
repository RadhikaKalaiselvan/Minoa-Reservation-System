    private void paymentScenario(){
    	controller.initPayment(startDateText.getText(), endDateText.getText());
        confirmChargesButton.setEnabled(true);
        cardDetPanel.setVisible(true);
        saveCard.setVisible(false);
    }
    private void submitCustActionPerformed(java.awt.event.ActionEvent evt) {                                           
        String name = custNameText.getText();
        String phone = phoneNoText.getText();
        String email = emailText.getText();
        custDetPanel.setVisible(false);
        float cost = controller.enterGuestDetails(name, email, phone);
        totalCostLabel.setText(""+cost);
        int rt = controller.paymentNeeded(); 
        if(rt == 1){
           this.paymentScenario(); 
        }
        else if(rt == 3 || rt == 4){
            cardDetPanel.setVisible(true);
            submitCard.setVisible(false);
            saveCard.setVisible(true);
        }
        else{
        	confirmButton.setEnabled(true);
        }
    }                                          

